// Get all state names
export const getAllStates = () => {
  return Object.keys(indianStatesAndCities).sort();
};

// Get cities for a specific state
export const getCitiesByState = (stateName) => {
  return indianStatesAndCities[stateName] || [];
};

// Search cities across all states
export const searchCities = (searchTerm) => {
  const results = [];
  const term = searchTerm.toLowerCase();
  
  Object.entries(indianStatesAndCities).forEach(([state, cities]) => {
    cities.forEach(city => {
      if (city.toLowerCase().includes(term)) {
        results.push({ city, state });
      }
    });
  });
  
  return results;
};

